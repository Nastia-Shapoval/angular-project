/// <reference types="cypress" />

describe('E2E: Новини ІТ-індустрії — повний сценарій', () => {

  it('користувач відкриває сайт і бачить список новин', () => {

    // 1. Відкриття сторінки новин
    cy.visit('/items');

    // 2. Перевірка заголовка сторінки
    cy.contains('Новини ІТ-індустрії');

    // 3. Перевірка, що новини відобразились
    cy.get('app-item-card')
      .should('exist')
      .and('have.length.at.least', 1);
  });

});

